    def get_chapter(self, chapter_id):
        """Получить главу по ID"""
        return self.chapters.get(chapter_id)
    
    def get_next_chapter(self, chapter_id, choice_index=None):
        """Получить следующую главу после выбора"""
        chapter = self.get_chapter(chapter_id)
        if not chapter:
            return None
        
        if choice_index is not None and chapter.choices:
            if 0 <= choice_index < len(chapter.choices):
                next_id = chapter.choices[choice_index]['next']
                return self.get_chapter(next_id)
        
        if chapter.next_chapter:
            return self.get_chapter(chapter.next_chapter)
        
        return None
