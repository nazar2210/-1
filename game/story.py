"""
Система сюжета игры-новеллы
"""


class StoryChapter:
    """Класс для представления главы сюжета"""
    
    def __init__(self, chapter_id, title, text, choices=None, next_chapter=None):
        self.chapter_id = chapter_id
        self.title = title
        self.text = text
        self.choices = choices or []  # Список выборов [{"text": "...", "next": "chapter_id"}, ...]
        self.next_chapter = next_chapter  # ID следующей главы, если нет выбора
    
    def has_choices(self):
        """Проверяет, есть ли у главы выборы"""
        return len(self.choices) > 0
    
    def get_text_with_choices(self):
        """Возвращает текст главы с вариантами выбора"""
        result = f"*{self.title}*\n\n{self.text}\n\n"
        if self.choices:
            result += "Выберите действие:\n"
            for i, choice in enumerate(self.choices, 1):
                result += f"{i}. {choice['text']}\n"
        return result


